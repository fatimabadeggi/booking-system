	function positionArrow()
	{
		var $homeBanner = $("#home-banner");
		
		if(elementInViewport2($homeBanner.get(0))){
			var $content = $(".content", $homeBanner),
				$arrow = $(".arrow", $homeBanner),
				arrowHeight = $arrow.outerHeight() + 18;
			
			if(window.innerWidth < 767)
			{
				$arrow.hide();
				return;
			}
			
			if($content.outerHeight() + arrowHeight + $content.offset().top > window.innerHeight){
				$arrow.hide();
				return;
			}
			
			if(window.innerHeight + $(window).scrollTop() > $homeBanner.outerHeight())
			{
				$arrow.css("bottom", 20);
				$arrow.css("top", "auto");
			}
			else
			{
				$arrow.css("bottom", "auto");
				$arrow.css("top", window.innerHeight - arrowHeight);
			}
			$arrow.show();
		}
	}
	
	function elementInViewport2(el) {
		var top = el.offsetTop;
		var left = el.offsetLeft;
		var width = el.offsetWidth;
		var height = el.offsetHeight;

		while(el.offsetParent) {
			el = el.offsetParent;
			top += el.offsetTop;
			left += el.offsetLeft;
		}

		return (
			top < (window.pageYOffset + window.innerHeight) &&
			left < (window.pageXOffset + window.innerWidth) &&
			(top + height) > window.pageYOffset &&
			(left + width) > window.pageXOffset
		);
	}

